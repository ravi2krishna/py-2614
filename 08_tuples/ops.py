# Tuples Operations / Methods

# index(): Used to get the index position of value 
data = (10,20,30,40,50)
print(data)
position = data.index(30)
print(position)
print(data.index(20))
print(data.index(50))

# count(): Count the number of occurrences 
data = (10,20,10,30,10,40,50)
print(data)
print(data.count(10))

